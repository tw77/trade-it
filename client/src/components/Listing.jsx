import { useEffect } from 'react';
import { useParams } from 'react-router-dom'

export default function Listing() {
  const {listingId} = useParams();

  useEffect(() => {
    axios.get(`listings/${listingId}`)
  }, [listingId]);


  // Propose a trade! button links to {ProposeTrade}
    
  return (
    <div>
      <h2>Listing</h2>
    </div>
  )
}